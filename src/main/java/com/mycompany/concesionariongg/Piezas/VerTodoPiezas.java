    private void obtenerTodosDatosPiezas() {
        //Se le da el modelo a la tabla a√±adiendole las columnas y las filas y se crea el filtro que saque todos los datos de las piezas
        Conexion conexion = new Conexion();
        FindIterable<Document> datos = conexion.col_Piezas.find();
        DefaultTableModel tabla = new DefaultTableModel();
        String[] columnas = {"Pieza", "Marca", "Modelo", "Anyo", "Precio"};
        tabla.setColumnIdentifiers(columnas);
        jTablePiezas.setModel(tabla);
        for (Document d : datos) {
            Object[] filas = {d.get("Pieza"), d.get("Marca"), d.get("Modelo"), d.get("Anyo"), d.get("Precio")};
            tabla.addRow(filas);
        }
        jTablePiezas.setFont(new Font("Arial", Font.BOLD, 12));
    }

